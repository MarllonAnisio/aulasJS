const nome = window.prompt("What is your name?");
document.body.innerHTML = `<h1>Hello, ${nome}!</h1>`;
document.body.innerHTML += `seu nome é: ${nome}`;
document.body.innerHTML += `<br>Seu nome tem ${nome.length} letras.`;
document.body.innerHTML += `<br>A segunda letra do seu nome é: ${nome.charAt(1)}`;
document.body.innerHTML += `<br>Qual o primeiro índice da letra "a" no seu nome? ${nome.indexOf("a")}`;
document.body.innerHTML += `<br>Qual o último índice da letra "a" no seu nome? ${nome.lastIndexOf("a")}`;
document.body.innerHTML += `<br>As últimas 3 letras do seu nome são: ${nome.slice(-3)}`;
document.body.innerHTML += `<br>As palavras do seu nome são: ${nome.split(" ")}`;
document.body.innerHTML += `<br>Seu nome em maiúsculas: ${nome.toUpperCase()}`;
document.body.innerHTML += `<br>Seu nome em minúsculas: ${nome.toLowerCase()}`;